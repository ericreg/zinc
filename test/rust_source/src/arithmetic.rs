fn main() {
    let a = 2 + 3;
    let b = 10 - 4;
    let c = 3 * 5;
    let d = 20 / 4;
    println!("a: {}, b: {}, c: {}, d: {}", a, b, c, d);
    let e = 3.14 + 2.0;
    let f = 10.0 / 4.0;
    println!("e: {}, f: {}", e, f);
    let g = (2 + 3) * 4;
    let h = 10 - 2 * 3;
    println!("g: {}, h: {}", g, h);
}
